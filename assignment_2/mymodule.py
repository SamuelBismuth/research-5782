# Python Module example

def add(a, b):
   """This program adds two
   numbers and return the result"""

   result = a + b
   return result


def substract(a, b):
   """This program substract two
   numbers and return the result"""

   result = a - b
   return result


def multiply(a, b):
   """This program multiply two
   numbers and return the result"""

   result = a * b
   return result